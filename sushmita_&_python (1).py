"""## 4. Abstraction

**Definition:** Abstraction is the process of hiding the complex implementation details and showing only the essential features of an object. It focuses on *what* an object does rather than *how* it does it. In Python, abstraction can be achieved using Abstract Base Classes (ABCs) from the `abc` module.

**Example:** Let's define an abstract `Shape` class with an abstract method `area()`. Concrete shapes like `Circle` and `Rectangle` will then implement this method.
"""

from abc import ABC, abstractmethod
import math

class Shape(ABC): # Inherit from ABC to make it an abstract class
    @abstractmethod
    def area(self):
        pass # Abstract method, must be implemented by subclasses

    @abstractmethod
    def perimeter(self):
        pass # Another abstract method

    def display_color(self):
        print("This shape has a color.")

class Circle(Shape):
    def __init__(self, radius):
        self.radius = radius

    def area(self):
        return math.pi * self.radius**2

    def perimeter(self):
        return 2 * math.pi * self.radius

class Rectangle(Shape):
    def __init__(self, width, height):
        self.width = width
        self.height = height

    def area(self):
        return self.width * self.height

    def perimeter(self):
        return 2 * (self.width + self.height)

# Demonstration
my_circle = Circle(5)
my_rectangle = Rectangle(4, 6)

print(f"Circle Area: {my_circle.area():.2f}")
print(f"Circle Perimeter: {my_circle.perimeter():.2f}")
my_circle.display_color() # Can use non-abstract method from base class

print("\n")

print(f"Rectangle Area: {my_rectangle.area()}")
print(f"Rectangle Perimeter: {my_rectangle.perimeter()}")
