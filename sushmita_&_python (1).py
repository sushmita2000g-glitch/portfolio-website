"""## Tree Data Structure Example: Binary Search Tree

A **Tree** is a hierarchical data structure consisting of nodes connected by edges. A **Binary Tree** is a tree data structure in which each node has at most two children, which are referred to as the left child and the right child.

A **Binary Search Tree (BST)** is a special type of binary tree where for each node:
- The value of all nodes in its left subtree is less than the node's value.
- The value of all nodes in its right subtree is greater than the node's value.
"""

class TreeNode:
    def __init__(self, value):
        self.value = value
        self.left = None
        self.right = None

class BinarySearchTree:
    def __init__(self):
        self.root = None

    def insert(self, value):
        if self.root is None:
            self.root = TreeNode(value)
        else:
            self._insert_recursive(self.root, value)

    def _insert_recursive(self, node, value):
        if value < node.value:
            if node.left is None:
                node.left = TreeNode(value)
            else:
                self._insert_recursive(node.left, value)
        elif value > node.value:
            if node.right is None:
                node.right = TreeNode(value)
            else:
                self._insert_recursive(node.right, value)
        # If value is equal, we typically don't insert duplicates in a simple BST

    def inorder_traversal(self):
        elements = []
        self._inorder_recursive(self.root, elements)
        return elements

    def _inorder_recursive(self, node, elements):
        if node:
            self._inorder_recursive(node.left, elements)
            elements.append(node.value)
            self._inorder_recursive(node.right, elements)

